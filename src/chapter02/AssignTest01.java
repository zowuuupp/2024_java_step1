package chapter02;

public class AssignTest01 {

	public static void main(String[] args) {
		// 대입연삽(복합연산). x += 10 -> x = x + 10 (제어문 들어갈때 많이 사용한다)
		
		int num1=10;
		int num2=20;
		
		System.out.println(num1);
		
		num1=num1+num2;
		System.out.println(num1);
		
		num1+=num2;
		System.out.println(num1);
		

	}

}

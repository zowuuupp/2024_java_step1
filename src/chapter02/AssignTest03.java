package chapter02;

public class AssignTest03 {

	public static void main(String[] args) {
		// 음수표시 만 할껀지 음수로 변환 할껀지
		
		int num1=10;
		int num2=20;
		
		System.out.println((+num1));
		System.out.println((-num2)); // 음수로 표시만 한거다. 특수문자만 붙인거다.
		System.out.println((num2));
				
		
		num2=-num2;
		
		System.out.println(num2); // 대입연산이 이루어졌다. 실제로 음수로 바꿔서 넣은거다.
		
		
		
		
		
		

	}

}

package chapter07;

public class Car {
	
	public String color;
	int cc;
	
	public Car() {
		
	}
	
	public Car(String color , int cc) {
		this.cc=cc;
		this.color=color;
	}
	
	
	//게터 세터
	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

	public int getCc() {
		return cc;
	}

	public void setCc(int cc) {
		this.cc = cc;
	}
	
	//메소드
	
	
	
	
	
	
	
	
	

}
